public class Variable {
    private int A;
    private int f;
    private double fi;

    public Variable(int a, int f, double fi) {
        A = a;
        this.f = f;
        this.fi = fi;
    }

    public int getA() {
        return A;
    }

    public int getF() {
        return f;
    }

    public double getFi() {
        return fi;
    }
}
